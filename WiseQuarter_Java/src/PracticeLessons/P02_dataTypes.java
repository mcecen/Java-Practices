package PracticeLessons;

public class P02_dataTypes {

    public static void main(String[] args) {

        // Primitive data türleri
        // byte-short-integer-long-float-double

        byte byteMAX= Byte.MAX_VALUE;
        System.out.println("byteMAX = " + byteMAX);

        byte byteMIN= Byte.MIN_VALUE;
        System.out.println("byteMIN = " + byteMIN);

        short shortMAX= Short.MAX_VALUE;
        short shortMIN= Short.MIN_VALUE;
        System.out.println("shortMAX = " + shortMAX);
        System.out.println("shortMIN = " + shortMIN);

        // char-boolean
        char chr1 = 65;
        char chr2 = 'A';

        System.out.println("chr1 = " + chr1);
        System.out.println("chr2 = " + chr2);

        boolean deger = false;
        System.out.println("deger = " + deger);

        // non-primitive data türleri
        // sadece string biliyoruz
        // çok fazla tür vardır
        // kendimizde non-primitive data türü ekleyebiliyoruz
        // hepsinin atası OBJECTS

    }
}
